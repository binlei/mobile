package com.jshuabo.mobile.server.model.batchinstall;

import com.jshuabo.frame.server.model.base.BaseStatus;
import com.jshuabo.frame.server.model.base.REntity;
import com.jshuabo.frame.server.model.organization.Organization;

/**
 * @ClassName: AppInfo
 * @Description: 
 * @author: peng.wu
 * @date: Apr 1, 2014 4:12:55 PM
 */
public class AppInfo extends REntity {
    private static final long serialVersionUID = -5084087683322784632L;
    
     /**
     * @fieldName: name
     * @fieldType: String
     * @Description: 应用名称
     */
    private String name;
    
    /**
     * @fieldName: type
     * @fieldType: AppType
     * @Description: 应用类型
     */
    private AppType appType;
    
     /**
     * @fieldName: organization
     * @fieldType: Organization
     * @Description: 渠道
     */
    private Organization organization;
    
     /**
     * @fieldName: showShortCut
     * @fieldType: Boolean
     * @Description: 是否创建快捷键
     */
    private Boolean showShorCut;
    
     /**
     * @fieldName: appIcoPath
     * @fieldType: String
     * @Description: 图标路径
     */
    private String icoPath;
    
     /**
     * @fieldName: appVersion
     * @fieldType: String
     * @Description: 应用版本
     */
    private String appVersion;
    
     /**
     * @fieldName: fileName
     * @fieldType: String
     * @Description: 文件名称
     */
    private String fileName;
    
     /**
     * @fieldName: filePackageName
     * @fieldType: String
     * @Description: 应用包名  com.XXX.OOO
     */
    private String filePackageName;
    
     /**
     * @fieldName: filePath
     * @fieldType: String
     * @Description: 文件路径
     */
    private String filePath;
    
     /**
     * @fieldName: fileSize
     * @fieldType: Double
     * @Description: 文件大小（单位：M）
     */
    private Double fileSize;
    
     /**
     * @fieldName: description
     * @fieldType: String
     * @Description: 描述
     */
    private String description;
    
     /**
     * @fieldName: status
     * @fieldType: String
     * @Description: 状态
     */
    private String status = BaseStatus.DISABLED;
    
 
     /**
     * @fieldName: MD5
     * @fieldType: String
     * @Description: 文件的MD5
     */
    private String md5;
    
     /**
     * @fieldName: extendProp2
     * @fieldType: String
     * @Description: extendProp2
     */
    private String extendProp2;

    public String getName() {
    return name;
    }

    public void setName(String name) {
    this.name = name;
    }

    public AppType getAppType() {
    return appType;
    }

    public void setAppType(AppType appType) {
    this.appType = appType;
    }

    public String getIcoPath() {
    return icoPath;
    }

    public void setIcoPath(String icoPath) {
    this.icoPath = icoPath;
    }

    public String getAppVersion() {
    return appVersion;
    }

    public void setAppVersion(String appVersion) {
    this.appVersion = appVersion;
    }

    public String getFileName() {
    return fileName;
    }

    public void setFileName(String fileName) {
    this.fileName = fileName;
    }

    public String getFilePath() {
    return filePath;
    }

    public void setFilePath(String filePath) {
    this.filePath = filePath;
    }

    public Double getFileSize() {
    return fileSize;
    }

    public void setFileSize(Double fileSize) {
    this.fileSize = fileSize;
    }

    public String getDescription() {
    return description;
    }

    public void setDescription(String description) {
    this.description = description;
    }

    public String getStatus() {
    return status;
    }

    public void setStatus(String status) {
    this.status = status;
    }


    public String getExtendProp2() {
    return extendProp2;
    }

    public void setExtendProp2(String extendProp2) {
    this.extendProp2 = extendProp2;
    }

    public Boolean getShowShorCut() {
        return showShorCut;
    }

    public void setShowShorCut(Boolean showShorCut) {
        this.showShorCut = showShorCut;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getFilePackageName() {
        return filePackageName;
    }

    public void setFilePackageName(String filePackageName) {
        this.filePackageName = filePackageName;
    }

    public Organization getOrganization() {
        return organization;
    }

    public void setOrganization(Organization organization) {
        this.organization = organization;
    }

}
